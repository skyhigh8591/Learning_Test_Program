//******** 3_2D.c *********************************
//*動作：使用暫存器名稱定義檔
//*硬體：SW1-3(P0LED)及SW1-4(P1LED)ON
//*************************************************
#include "..\MPC82.H"  //暫存器及組態設定
void main()
{  
   unsigned char i=0;
   P0M0=0; P0M1=0xFF; //設定P0為推挽式輸出(M0-1=01)
   loop:
   P0=~i;  Delay_ms(100);
   i=i+1;

   P1_1=0;  Delay_ms(100);
   P1_1=1;  Delay_ms(100); 
   goto loop;  //跳到loop處 
}

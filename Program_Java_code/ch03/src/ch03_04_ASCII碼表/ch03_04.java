package ch03_04_ASCIIX;

public class ch03_04 {

	public static void main(String[] args) {
		char ch1;
		ch1 = 'a';						
		System.out.println("跑计ch氦寒e = " + ch1);
		ch1 = 'x';						
		System.out.println("跑计ch氦寒e = " + ch1);
		
		char ch2;
		ch2 = 65;						
		System.out.println("跑计ch氦寒e = " + ch2);
		
		char ch3;
		ch3 = '\u0041'; //UnicoderじC						
		System.out.println("跑计ch氦寒e = " + ch3);
		ch3 = '\u9B41';	//UnicoderじC					
		System.out.println("跑计ch氦寒e = " + ch3);
	}
}
